package org.wso2.financial.services.accelerator.consent.mgt.extensions.manage.filters;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.wso2.financial.services.accelerator.common.json.schema.validator.RequestWrapper;

import java.io.IOException;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.http.HttpServletRequest;

/**
 * Filter to validate the consent API schema.
 */
public class ConsentAPISchemaValidationFilter implements Filter {

    private static final Log log = LogFactory.getLog(ConsentAPISchemaValidationFilter.class);

    @Override
    public void doFilter(ServletRequest servletRequest, ServletResponse servletResponse, FilterChain filterChain)
            throws IOException, ServletException {

        if (servletRequest instanceof HttpServletRequest) {
            RequestWrapper cachedRequest = new RequestWrapper((HttpServletRequest) servletRequest);

            filterChain.doFilter(cachedRequest, servletResponse);
        }
    }
}
